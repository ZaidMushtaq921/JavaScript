// != or <=> not equal   - returns true if the operands are not equal
// >  greater than      - returns true if the left operand is greater than right operand
// >= or >==            - returns true if  the left operand is greater than or equal to the right operand
// < less then           - returns true if the left operand is less than the right operand
// <= or <<=             - returns true if the left operand is less than or equal to the right operand.



